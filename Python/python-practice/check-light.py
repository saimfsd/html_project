
while True:
    value = input("Enter  a value start aur not : ")

    if value == "Start":
        print("Power on")

    elif value == "right":
        print("Right light is on")

    elif value == "left":
        print("Left light is on")

    elif value == "Reverse":
        print("Reverse light is on")
  
    else:
        print("Power off")
        break 